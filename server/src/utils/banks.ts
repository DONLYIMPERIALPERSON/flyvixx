export const sortedBanks = banks.sort((a, b) => a.name.localeCompare(b.name));

// Helper function to get bank code by name
export function getBankCodeByName(bankName: string): string | null {
  const bank = banks.find(b => b.name.toLowerCase() === bankName.toLowerCase());
  return bank ? bank.bankCode : null;
}